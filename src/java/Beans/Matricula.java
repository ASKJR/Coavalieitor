package Beans;

import java.io.Serializable;

/**
 *
 * @author Kato
 */
public class Matricula  implements Serializable {
    
    private Aluno aluno;
    private Turma turma;
    
    public Matricula(){
        aluno = new Aluno();
        turma = new Turma();
    }
    
    public Aluno getAluno() {
        return aluno;
    }

    public void setAluno(Aluno aluno) {
        this.aluno = aluno;
    }

    public Turma getTurma() {
        return turma;
    }

    public void setTurma(Turma turma) {
        this.turma = turma;
    }
    
}
